# манипуляции с цитатой

quote="Думаю, что на мировом рынке, можно будет продать штук пять компьютеров";

print("Исходная цитата:");
print(quote);

print("\n Она же в верхнем регистре:");
print(quote.upper());

print("\n Она же в нижнем регистре:");
print(quote.lower());

print("\n цитата как заголовок:");
print(quote.title());

print("\n цитата с некоторой модификацией:");
print(quote.replace("штук пять","несколько миллионов"));

print("Опять исходная цитата:");
print(quote);

